def rate_limit(limit_string: str):
    """
    ‚úÖ Wrapper profissional que garante compatibilidade total

    Uso:
    ```python
    @router.post("/login")
    @rate_limit("5/minute")
    async def login(...):
        return {"token": "..."}  # Retorna dict normal
    ```
    """
    def decorator(func: Callable) -> Callable:
        # ‚úÖ Aplica limiter mas n√£o tenta modificar resposta dict
        limited_func = limiter.limit(limit_string)(func)

        # ‚úÖ Marca fun√ß√£o para FastAPI processar corretamente
        limited_func.__rate_limited__ = True
        limited_func.__rate_limit__ = limit_string

        return limited_func

    return decorator



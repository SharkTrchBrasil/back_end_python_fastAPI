async def rate_limit_exceeded_handler(request: Request, exc: RateLimitExceeded) -> JSONResponse:
    """
    ‚úÖ Handler ass√≠ncrono para rate limit excedido

    Retorna JSONResponse diretamente (compat√≠vel com FastAPI)
    """
    path = request.url.path
    method = request.method
    identifier = get_identifier(request)

    # Log de seguran√ßa
    logger.warning(
        f"üö® RATE LIMIT EXCEDIDO\n"
        f"   ‚îú‚îÄ Path: {method} {path}\n"
        f"   ‚îú‚îÄ Identificador: {identifier}\n"
        f"   ‚îú‚îÄ Limite: {exc.detail}\n"
        f"   ‚îî‚îÄ User-Agent: {request.headers.get('user-agent', 'N/A')[:100]}"
    )

    # ‚úÖ Extrai informa√ß√µes do erro
    retry_after = 60  # Padr√£o: 1 minuto
    try:
        if hasattr(exc, 'retry_after'):
            retry_after = int(exc.retry_after)
        elif "Retry after" in exc.detail:
            retry_after = int(exc.detail.split("Retry after ")[1].split(" ")[0])
    except:
        pass

    # ‚úÖ Resposta JSON profissional
    return JSONResponse(
        status_code=429,
        content={
            "error": "too_many_requests",
            "message": "Muitas requisi√ß√µes. Por favor, aguarde alguns segundos.",
            "detail": exc.detail,
            "retry_after_seconds": retry_after,
        },
        headers={
            "Retry-After": str(retry_after),
            "X-RateLimit-Remaining": "0",
        }
    )



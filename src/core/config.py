def validate_config():
    """Valida configura√ß√µes cr√≠ticas"""
    errors = []

    if len(config.SECRET_KEY) < 32:
        errors.append("SECRET_KEY muito curta (m√≠nimo 32 caracteres)")

    if not config.PAGARME_SECRET_KEY.startswith("sk_"):
        errors.append("PAGARME_SECRET_KEY inv√°lida")

    if config.ENVIRONMENT not in ["development", "test", "production"]:
        errors.append("ENVIRONMENT deve ser: development, test ou production")

    if errors:
        raise ValueError(
            "‚ùå Erros de configura√ß√£o:\n" + "\n".join(f"  ‚Ä¢ {e}" for e in errors)
        )


